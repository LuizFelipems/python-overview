# Tupla é uma Lista imutável. Ex: lista de e-mails.

a = (1,2, 3,4,5,6,7,'texto',7,7,7)

print(a[0]) # Retorna o elemento do index passado
print(a.index('texto')) # Retorna o primeiro index do elemento passado
print(a.count(3)) # Quantas vezes o elemento aparece
print(dir(a)) # O que pode ser feito com o objeto
